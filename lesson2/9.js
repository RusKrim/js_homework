// Отсортируйте массив по убыванию.
// Использовать встроенные методы массивов — нельзя.

////////////////// Задание //////////////////
const arr = [1, 2, 3, 4, 5, 6];
// [6,5,4,3,2,1]

////////////////// Решение //////////////////
for (let i = 0; i < arr.length; i++) {
  for (let k = 0; k < arr.length; k++) {
    if (arr[k] < arr[i]) {
      let min = arr[k];
      arr[k] = arr[i];
      arr[i] = min;
    }
  }
}
