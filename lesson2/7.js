// Напишите код, который посчитает сумму всех парных элементов в массиве.
// Использовать встроенные методы массивов — нельзя.

////////////////// Задание //////////////////
const array = [1, 2, 3, 4];

////////////////// Решение //////////////////
let sum = null;

for (let n of array) {
  if (n % 2 === 0) sum += n;
}

console.log(sum);
